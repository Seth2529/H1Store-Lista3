//A injeção de dependência (DI) é uma técnica usada na camada de aplicação para gerenciar as dependências de uma classe. 
//Isso é feito injetando explicitamente objetos ou serviços de que a classe depende, em vez de criá-los internamente. 
//A principal vantagem da DI é desacoplar classes, tornando o código mais flexível, testável e fácil de manter. 
//Ela facilita a substituição de implementações de dependências, aprimora a testabilidade, promove a reutilização de 
//código e é fundamental em padrões de design como Inversão de Controle (IoC) e Controle de Inversão de Dependência (DIC).
